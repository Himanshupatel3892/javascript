// Area of Square
let side = 5;
let areaSquare = side * side;
console.log("Area of Square:", areaSquare);

// Area of Triangle
let base = 10;
let height = 8;
let areaTriangle = 0.5 * base * height;
console.log("Area of Triangle:", areaTriangle);

// Area of Rectangle
let length = 12;
let breadth = 6;
let areaRectangle = length * breadth;
console.log("Area of Rectangle:", areaRectangle);

// Area of Circle
let radius = 7;
let areaCircle = 3.14 * radius * radius;
console.log("Area of Circle:", areaCircle);
